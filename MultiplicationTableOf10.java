package KrishnaSandeepJavaPrograms;



class MultiplicationTableOf10{
	  
	  public static void main(String[] args){
	    
	    int n=10;
	    
	    int result=1;
	    
	    for(int i=1;i<=n;i++){
	      result=n*i;
	      
	      System.out.println(+n+ " X "+i+" = "+result);
	      
	      
	      
	    }
	    
	  }
	}
