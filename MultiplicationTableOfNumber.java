package KrishnaSandeepJavaPrograms;



class MultiplicationTableOfNumber{
	  
	  public static void main(String[] args){
	    
	    int num=5;
	    
	    int n=10;
	    
	    int result=0;
	    
	    for(int i=1;i<=n;i++){
	      
	      result=num*i;
	      
	     
	     
	     System.out.println(num+" X "+i+" = "+result);
	      
	    }
	    
	    
	  }
	  
	}
